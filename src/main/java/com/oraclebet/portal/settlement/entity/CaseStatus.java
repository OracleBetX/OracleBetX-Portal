package com.oraclebet.portal.settlement.entity;


/**
 * 客服/人工介入工单状态（order_reconcile_case.case_status）。
 *
 * 设计目的：
 * - 用于管理“自动收敛失败后”升级到人工处理的订单；
 * - 区分案件当前所处的人工处理阶段；
 * - 便于后台工作台筛选、统计与分配。
 *
 * 注意：
 * - 只有当自动 Reconcile 重试超过阈值或出现严重异常时，
 *   才会进入 Case（人工工单）体系；
 * - CaseStatus 不影响资金真相（Ledger/RSV），
 *   仅表示“人工处理流程状态”。
 */
public enum CaseStatus {

    /**
     * OPEN：新建工单，待处理。
     *
     * 含义：
     * - 系统检测到异常且自动收敛失败；
     * - 工单刚刚创建；
     * - 尚未被客服或工程人员接手。
     *
     * 常见来源：
     * - 自动重试次数耗尽；
     * - RSV 状态异常；
     * - 订单与资金状态长期不一致。
     */
    OPEN,

    /**
     * PROCESSING：处理中。
     *
     * 含义：
     * - 工单已被人工接手；
     * - 正在排查或执行修复操作；
     * - 可能涉及手动重试、数据核对、日志排查等。
     *
     * 说明：
     * - 可记录 assignee（处理人）与操作日志。
     */
    PROCESSING,

    /**
     * RESOLVED：已解决。
     *
     * 含义：
     * - 订单状态已成功收敛；
     * - 资金/RSV 状态确认一致；
     * - 无需进一步人工干预。
     *
     * 注意：
     * - 进入该状态前应确保：
     *   1) 订单已处于终态（CANCELLED / FILLED / CLOSED）；
     *   2) RSV 已处于终态（RELEASED / COMMITTED）。
     */
    RESOLVED,

    /**
     * IGNORED：忽略处理。
     *
     * 含义：
     * - 经人工判断，该异常无需处理；
     * - 例如：重复上报、误报、历史已收敛但日志滞后等。
     *
     * 注意：
     * - 使用该状态应保留备注（notes），说明忽略原因；
     * - 不应滥用，避免掩盖真实系统问题。
     */
    IGNORED
}